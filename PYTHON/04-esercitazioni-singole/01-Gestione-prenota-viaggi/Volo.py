class Volo : 
    __id_voli = int()
    __nomeCompagnia = str()
    __partenze = str()
    __arrivi = str()
    __prezzo = float()

def __init__(self, id_voli, nomeCompagnia, partenze, arrivi, prezzo) :
    self.id_voli = id_voli
    self.nomeCompagnia = nomeCompagnia
    self.partenze = partenze
    self.arrivi = arrivi
    self.prezzo = prezzo

