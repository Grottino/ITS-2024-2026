class NoleggioAuto():
    __id_noleggio = int()
    __nome_azienda = str()
    __available_cars = int()
    __car_type = str()
    __targa = str()
    __prezzo = float()

def __init__(self, id_noleggio, nome_azienda, available_cars, car_type, targa, prezzo):
    self.id_noleggio = id_noleggio
    self.nome_azienda = nome_azienda
    self.available_cars = available_cars
    self.car_type = car_type
    self.targa = targa
    self.prezzo = prezzo