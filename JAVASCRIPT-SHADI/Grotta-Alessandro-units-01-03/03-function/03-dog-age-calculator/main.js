// Declare a variable to hold the dog's age
let dogAge;

// Function to calculate the dog's age in dog years
function calculateDogAge(dogAge) {
    // Multiply the dog's age by 7 to get the age in dog years
    dogAge = dogAge * 7;
    // Log the calculated dog age to the console
    console.log("Your dog is " +  dogAge + " years old in dog years");
}

// Calculate and log the dog age for a dog that is 12 years old
calculateDogAge(12);

// Calculate and log the dog age for a dog that is 3 years old
calculateDogAge(3);

// Calculate and log the dog age for a dog that is 7 years old
calculateDogAge(7);