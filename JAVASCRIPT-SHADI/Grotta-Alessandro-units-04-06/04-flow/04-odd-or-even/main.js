// Initialize a variable i with the value 0
let i = 0;

// Use a for loop to iterate from 0 to 20
for (; i <= 20; i++) {
    // Check if the current value of i is even
    if (i % 2 == 0) {
        // If true, log that the number is even
        console.log(i + " is even");
    } else {
        // If false, log that the number is odd
        console.log(i + " is odd");
    }
}